<script setup>
import {
    ref,
    computed,
    onMounted
} from 'vue';
import {
    onPullDownRefresh,
    onReachBottom
} from '@dcloudio/uni-app';

import {
    apiGetPlanPage
} from '@/api/apis.js';

import { useUserStore } from '@/stores/user.js'

//3 预约中 0 待收运 1 已完成
const tabs = [{ key: "3", value: "预约中" }, { key: "0", value: "进行中" }, { key: "1", value: "已完成" }];
//下标
const currentTab = ref(0);

const userStore = useUserStore();

//返回上一页
const back = () => {
    uni.navigateBack()
}

//点击菜单
function handleTabClick(index) {
    currentTab.value = index;
    allOrderList.value = [];
    pageNum.value = 1; // 重置页码为1
    getNetwork();
}

// 添加badgeText的ref变量
const bookingBadgeText = ref('0');
const processingBadgeText = ref('0');

// 添加页码和加载状态变量
const pageNum = ref(1);
const loadingStatus = ref('more'); // more-加载前/loading-加载中/nomore-没有更多数据

// 数据列表
const allOrderList = ref([]);

//后获取数据
const getNetwork = async () => {
    try {
        // 如果不是第一页，设置加载状态为加载中
        if (pageNum.value > 1) {
            loadingStatus.value = 'loading';
        }


        const res = await apiGetPlanPage({
            pageNum: pageNum.value,
            merchantId: userStore.merchant?.id,
            status: tabs[currentTab.value].key // 使用tabs中的key值
        });


        // 处理下拉刷新
        if (pageNum.value === 1) {
            allOrderList.value = res.data.records || [];
            uni.stopPullDownRefresh();
        } else {
            // 处理上拉加载更多
            allOrderList.value = [...allOrderList.value, ...(res.data.records || [])];
        }

        // 判断是否还有更多数据
        if (res.data.records && res.data.records.length < 10) {
            // 如果返回的数据少于每页数量，说明没有更多数据了
            loadingStatus.value = 'nomore';
        } else {
            // 否则还有更多数据
            loadingStatus.value = 'more';
        }
    } catch (error) {
        console.error('获取数据失败:', error);

        // 停止下拉刷新
        uni.stopPullDownRefresh();

        // 重置加载状态
        loadingStatus.value = 'more';

        // 如果是第一页加载失败，确保显示暂无数据状态
        if (pageNum.value === 1) {
            allOrderList.value = [];
        }

        uni.showToast({
            title: '数据加载失败，请重试',
            icon: 'none',
            duration: 2000
        });
    }
};


// 上拉加载更多方法
const onLoadMore = () => {
    if (loadingStatus.value === 'nomore') return;
    // 页码增加
    pageNum.value++;
    getNetwork();
};


// 触底加载更多
onReachBottom(() => {
    onLoadMore();
});

//下拉刷新
onPullDownRefresh(() => {
    allOrderList.value = [];
    currentTab.value = 0;
    pageNum.value = 1; // 重置页码为1
    getNetwork();
})

// 组件挂载时初始化数据
onMounted(() => {
    pageNum.value = 1;
    getNetwork();
});
</script>

