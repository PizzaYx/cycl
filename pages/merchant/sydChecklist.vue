            <view class="tab-bar">
                <view v-for="(tab, index) in tabs" :key="index" class="tab-item"
                    :class="{ active: currentTab === index }" @click="handleTabClick(index)">
                    {{ tab.value }}
                    <!-- 为预约中tab添加uni-badge -->
